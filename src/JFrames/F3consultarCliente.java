        txtObservaciones.setText("");
        txtServicios.setText("");
        txtIndicaciones.setText("");

        modelo.addRow(new Object[]{cc.getFechaConsulta(),cc.getHoraConsulta(),cc.getNombre(),
            cc.getApellido(),cc.getDNI(),cc.getMotivo(),cc.getObservaciones(),cc.getServicio(),cc.getIndicacines()});

